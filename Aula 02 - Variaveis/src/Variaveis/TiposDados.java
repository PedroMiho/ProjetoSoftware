package Variaveis;

public class TiposDados {

	public static void main(String[] args) {
		// Tipos Inteiros
		// byte = - 128 - 127
		byte numero = 127;
		
		// short  = -32.768 - 32.767
		short n2 = 300;
		
		// int = -2.147.483.648 - 2.147.483.647 
		int idade = 40;
		
		//long 
		long n3 = 500;
		
		// Tipos Decimais
		//float = -3,4 * 10^38 - 3,4 * 10^38 
		float peso = 30.60f ;
		//double -1,8 * 10 ^ 308 - 1,8 * 10 ^ 308
		double altura = 1.70;
		
		//Tipos Texto
		// "" - String
		// '' - char
		char genero = 'F';
		String nome = "Pedro";
		
		//Tipo booleano
		boolean logico = true;
	}

}
